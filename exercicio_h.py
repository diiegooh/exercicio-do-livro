#Elaborar um programa que calcule e apresente o valor do volume de uma caixa retangular, utilizando a fórmula VOLUME= COMPRIMENTO* LARGURA* ALTURA.

c = float(input("Digite o valor do COMPRIMENTO: "))
l = float(input("Digite o valor da LARGURA: "))
a = float(input("Digite o valor da ALTURA: "))

volume = c * l * a

print(f"O volume da caixa é : {volume:.2f}")

                
